#!/usr/bin/env bash
# save-session.sh — Save session summary for cross-machine continuity
# Run this at the END of every Claude session (Cowork / Claude Code)
# The next session on ANY machine will pick this up via memory.sh
# Usage: ./save-session.sh "what I worked on" "what's next"
set -uo pipefail

PROJECT_DIR="$(cd "$(dirname "$0")" && pwd)"
cd "$PROJECT_DIR" || exit 1

HOSTNAME=$(hostname -s 2>/dev/null || echo "unknown")
USER=$(whoami)
NOW=$(date '+%Y-%m-%d %H:%M:%S')
TODAY=$(date '+%Y-%m-%d')
BRANCH=$(git rev-parse --abbrev-ref HEAD 2>/dev/null || echo "main")
SESSION_DIR=".session-log"
SESSION_FILE="$SESSION_DIR/latest.md"
ARCHIVE_FILE="$SESSION_DIR/$TODAY-$(date '+%H%M')-$HOSTNAME.md"

mkdir -p "$SESSION_DIR"

# ─── Gather context ───

LAST_COMMITS=$(git log --since="6 hours ago" --oneline --all 2>/dev/null | head -15)
COMMIT_COUNT=$(echo "$LAST_COMMITS" | grep -c . 2>/dev/null || echo "0")
UNCOMMITTED=$(git status --short 2>/dev/null | head -10)
UNCOMMITTED_COUNT=$(git status --porcelain 2>/dev/null | wc -l | tr -d ' ')

# ─── Session summary from args or interactive ───

WORK_DONE="${1:-}"
NEXT_STEPS="${2:-}"

if [ -z "$WORK_DONE" ]; then
    echo "📝 What did you work on this session?"
    echo "   (press Enter for auto-summary from git)"
    read -r WORK_DONE
    if [ -z "$WORK_DONE" ]; then
        WORK_DONE="See commits below"
    fi
fi

if [ -z "$NEXT_STEPS" ]; then
    echo "📋 What's next? (press Enter to skip)"
    read -r NEXT_STEPS
    if [ -z "$NEXT_STEPS" ]; then
        NEXT_STEPS="Continue from last task"
    fi
fi

# ─── Write session file ───

cat > "$SESSION_FILE" << ENTRY
# Last Session — Cross-Machine Sync

## Session Info
- **Date:** $NOW
- **Machine:** $HOSTNAME ($USER)
- **Branch:** $BRANCH
- **Commits this session:** $COMMIT_COUNT

## What was done
$WORK_DONE

## Recent commits
$(if [ -n "$LAST_COMMITS" ] && [ "$LAST_COMMITS" != "" ]; then
  echo "$LAST_COMMITS" | while IFS= read -r line; do echo "- $line"; done
else
  echo "- No recent commits"
fi)

## Uncommitted work
$(if [ "$UNCOMMITTED_COUNT" -gt 0 ]; then
  echo "\`\`\`"
  echo "$UNCOMMITTED"
  echo "\`\`\`"
else
  echo "All clean"
fi)

## Next steps
$NEXT_STEPS

---
*Auto-generated by save-session.sh*
ENTRY

# ─── Archive ───
cp "$SESSION_FILE" "$ARCHIVE_FILE"

# ─── Commit & push ───
git add "$SESSION_DIR/" 2>/dev/null
git commit -m "chore(session): save session from $HOSTNAME ($TODAY)" -- "$SESSION_DIR/" 2>/dev/null || true
git push origin "$BRANCH" 2>/dev/null || echo "⚠️  Push failed — will sync later"

echo ""
echo "✅ Session saved to $SESSION_FILE"
echo "📦 Archived to $ARCHIVE_FILE"
echo "🔄 Next machine: run ./memory.sh to pick up this context"
ENTRY

